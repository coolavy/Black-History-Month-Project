st.subheader("Lift Every Voice and Sing")

poem1 = """
Lift every voice and sing\n
Till earth and heaven ring,\n
Ring with the harmonies of Liberty;\n
Let our rejoicing rise\n
High as the listening skies,\n
Let it resound loud as the rolling sea.\n
Sing a song full of the faith that the dark past has taught us,\n
Sing a song full of the hope that the present has brought us.\n
Facing the rising sun of our new day begun,\n
Let us march on till victory is won.\n
Stony the road we trod,\n
Bitter the chastening rod,\n
Felt in the days when hope unborn had died;\n
Yet with a steady beat,\n
Have not our weary feet\n
Come to the place for which our fathers sighed?\n
We have come over a way that with tears has been watered,\n
We have come, treading our path through the blood of the slaughtered,\n
Out from the gloomy past,\n
Till now we stand at last\n
Where the white gleam of our bright star is cast.\n\n
God of our weary years,\n
God of our silent tears,\n
Thou who hast brought us thus far on the way;\n
Thou who hast by Thy might\n
Led us into the light,\n
Keep us forever in the path, we pray.\n
Lest our feet stray from the places, our God, where we met Thee,\n
Lest, our hearts drunk with the wine of the world, we forget Thee;\n
Shadowed beneath Thy hand,\n
May we forever stand.\n
True to our God,\n
True to our native land.\n
"""

st.markdown(poem1)

